# Funções de cálculo de áreas (Sub-rotinas)

def calcular_area_retangulo(base, altura):
    """Sub-rotina que calcula a área de um retângulo."""
    return base * altura

def calcular_area_circulo(raio):
    """Sub-rotina que calcula a área de um círculo."""
    return 3.14159 * (raio ** 2)

def calcular_area_triangulo(base, altura):
    """Sub-rotina que calcula a área de um triângulo."""
    return (base * altura) / 2
